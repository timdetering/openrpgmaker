void ImportRM2K3Tileset::runImporter()
{
	QString fileLocation = QFileDialog::getOpenFileName(0, "Choose an RPG Maker 2000/2003 Tileset", QString(), "PNG Files (*.png *.PNG)");
	if (!fileLocation.isEmpty())
		importRM2K3Tileset(fileLocation);
}

QString ImportRM2K3Tileset::getImporterName()
{
	return "RPG Maker 2000/2003 Tileset";
}

QString ImportRM2K3Tileset::getImporterDescription()
{
	return "Import an RPG Maker 2000/2003 formatted tileset.";
}

